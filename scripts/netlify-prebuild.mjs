/**
 * Netlify: proxy same-origin /api/* to your Express host so uploads + bookings work
 * without baking VITE_API_URL into the client.
 *
 * Set on Netlify → Site configuration → Environment variables:
 *   NETLIFY_API_ORIGIN=https://your-api.onrender.com   (no trailing slash, no /api suffix)
 *
 * Then the build writes frontend/public/_redirects before Vite runs.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const outFile = path.join(root, 'frontend', 'public', '_redirects');

const raw = (process.env.NETLIFY_API_ORIGIN || '').trim().replace(/\/$/, '');
if (!raw) {
  // eslint-disable-next-line no-console
  console.warn(
    '[netlify-prebuild] NETLIFY_API_ORIGIN is not set. Browser calls to /api/* will hit Netlify and return 404 unless you set VITE_API_URL at build time to your full API base (…/api).',
  );
  process.exit(0);
}

try {
  // eslint-disable-next-line no-new-url
  new URL(raw);
} catch {
  // eslint-disable-next-line no-console
  console.error('[netlify-prebuild] NETLIFY_API_ORIGIN must be a valid absolute URL, e.g. https://api.example.com');
  process.exit(1);
}

const lines = [
  `# Generated by scripts/netlify-prebuild.mjs — do not edit (set NETLIFY_API_ORIGIN on Netlify)`,
  `/api/*  ${raw}/api/:splat  200`,
  ``,
];

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, lines.join('\n'), 'utf8');
// eslint-disable-next-line no-console
console.log(`[netlify-prebuild] wrote ${path.relative(root, outFile)} → ${raw}/api/*`);
