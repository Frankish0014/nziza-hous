/**
 * Netlify: write `frontend/public/_redirects` before Vite runs.
 *
 * - `/api/*` → your Express host (only when NETLIFY_API_ORIGIN is set)
 * - `/*` → SPA fallback (must be last so /api matches first)
 *
 * Set on Netlify → Site configuration → Environment variables:
 *   NETLIFY_API_ORIGIN=https://your-api.onrender.com   (no trailing slash, no /api suffix)
 *
 * Without NETLIFY_API_ORIGIN, browsers still call same-origin `/api/...` and Netlify will
 * return 404 for POST /api/messages etc. Set the variable (or use VITE_API_URL to a public …/api).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const outFile = path.join(root, 'frontend', 'public', '_redirects');

const raw = (process.env.NETLIFY_API_ORIGIN || '').trim().replace(/\/$/, '');

const lines = [
  '# Generated by scripts/netlify-prebuild.mjs — do not hand-edit (configure NETLIFY_API_ORIGIN on Netlify).',
];

if (raw) {
  try {
    // eslint-disable-next-line no-new-url
    new URL(raw);
  } catch {
    // eslint-disable-next-line no-console
    console.error(
      '[netlify-prebuild] NETLIFY_API_ORIGIN must be a valid absolute URL, e.g. https://api.example.com',
    );
    process.exit(1);
  }
  lines.push(`/api/*  ${raw}/api/:splat  200`);
  // eslint-disable-next-line no-console
  console.log(`[netlify-prebuild] proxy /api/* → ${raw}/api/*`);
} else {
  // eslint-disable-next-line no-console
  console.warn(
    '[netlify-prebuild] NETLIFY_API_ORIGIN is not set. Same-origin POST /api/messages (contact form), auth, and bookings will 404 on Netlify until you set NETLIFY_API_ORIGIN (e.g. https://your-app.onrender.com) or set VITE_API_URL at build time to your public API base (…/api, not localhost). Redeploy after saving env vars.',
  );
}

lines.push('/*  /index.html  200', '');

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, lines.join('\n'), 'utf8');
// eslint-disable-next-line no-console
console.log(`[netlify-prebuild] wrote ${path.relative(root, outFile)}`);
